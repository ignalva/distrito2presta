SELECT O.ID, O.OPERACION, O.TABLA, K.CAMPO, K.VALOR, NULL NEW_VALUE FROM DK$OPERATIONLOG O INNER JOIN DK$KEYLOG K ON O.ID=K.OPER_ID WHERE O.OPERACION<>2 UNION SELECT OP.ID, OP.OPERACION, OP.TABLA, C.CAMPO, C.OLD_VALUE, C.NEW_VALUE VALOR FROM DK$OPERATIONLOG OP INNER JOIN DK$COLUMNLOG C ON OP.ID=C.OPER_ID WHERE OP.OPERACION<>0 ORDER BY 1 ASC

DELETE FROM DK$OPERATIONLOG;

