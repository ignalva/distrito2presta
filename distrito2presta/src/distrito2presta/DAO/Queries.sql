/* SQLFEATURES */
SELECT IC.NOMBRE IDCARACT, ICV.VALORTRADUC IDFEATURE, C.CODCARACT, C.VALOR 
FROM CARVALOR C, IDIOMACARACT IC, IDIOMACARVALID ICV 
WHERE 
C.CODCLASE=ICV.CODCLASE AND 
C.CODCARACT=ICV.CODCARACT AND 
C.VALOR=ICV.VALORCARVALID AND 
IC.CODIDIOMA=ICV.CODIDIOMA AND 
IC.CODCLASE=ICV.CODCLASE AND 
IC.CODCARACT=ICV.CODCARACT AND 
ICV.CODIDIOMA=1 AND 
ICV.CODCLASE=2 AND 
C.CODOBJETO='066420326205' 
ORDER BY IDCARACT ASC, IDFEATURE ASC

/* SQLFIESTAEVENTO */
SELECT IT.DESCRIPCION ID FROM IDIOMATIPO IT, IDIOMACARVALID ICV, CARVALOR CV WHERE CV.VALOR=ICV.VALORCARVALID AND ICV.CODCLASE=CV.CODCLASE AND ICV.CODOBJETO IS NULL AND ICV.CODCARACT=CV.CODCARACT AND ICV.DIMENSION IS NULL AND ICV.VALORTRADUC=IT.METATITLE AND IT.CODIDIOMA=ICV.CODIDIOMA AND CV.CODCLASE=2 AND CV.CODOBJETO='02' AND ICV.CODIDIOMA='1' AND IT.TIPO=13

